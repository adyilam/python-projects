# list sort, sorts the list alphabetically
name_list = ["Smith", "Tom", "Zack", "Bob"]
name_list.sort()
print(name_list)

# list sort, sorts the list numerically
nums = [10, 8, 4, 2, 3, 6, 1, 5]
nums.sort()
print(nums)

# sort the list descending order
name_list = ["Smith", "Tom", "Zack", "Bob"]
name_list.sort(reverse=True)
print(name_list)


