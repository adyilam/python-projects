# Text Type:	str
# Numeric Types:	int, float, complex
# Sequence Types:	list, tuple, range
# Mapping Type:	dict
# Set Types:	set, frozenset
# Boolean Type:	bool
# Binary Types:	bytes, bytearray, memoryview
# None Type:	NoneType

# Example
x = "String"
print(x)
print(type(x))  # to get data type of x

y = 5
print(y)
print(type(y))

z = 5.0
print(z)
print(type(z))

items = ["pen", "book", "pencil"]
print(items)
print(type(items))

items = ("pen", "book", "pencil")
print(items)
print(type(items))

items = {"pen": 3, "book": 6}
print(items)
print(type(items))

items = {"pen", "book", "pencil"}
print(items)
print(type(items))

x = False
print(x)
print(type(x))
